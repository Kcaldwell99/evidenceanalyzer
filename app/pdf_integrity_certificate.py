def _redact_email(email: str) -> str:
    """k***@caldwell-law-firm.com"""
    if not email or "@" not in email:
        return email or "—"
    local, domain = email.split("@", 1)
    return f"{local[0]}***@{domain}"


def _fmt_exposure(value):
    """Format EXIF ExposureTime as a conventional shutter-speed string."""
    if not value:
        return "—"
    # Already a fraction string like "1/4115" — keep as-is
    s = str(value).strip()
    if "/" in s:
        return f"{s}s"
    try:
        x = float(value)
    except (TypeError, ValueError):
        return f"{s}s"
    if x <= 0:
        return f"{s}s"
    if x >= 1:
        # Long exposure: show as seconds, trim trailing zeros
        return f"{x:.1f}".rstrip("0").rstrip(".") + "s"
    # Fast shutter: convert to 1/N
    denom = round(1 / x)
    return f"1/{denom}s"


def _fmt_bytes(size_bytes) -> str:
    try:
        b = int(size_bytes)
        if b < 1024:
            return f"{b} bytes"
        elif b < 1024 ** 2:
            return f"{b / 1024:.1f} KB"
        else:
            return f"{b / 1024 ** 2:.1f} MB"
    except Exception:
        return str(size_bytes)


def _generate_qr(url: str) -> Image:
    qr = qrcode.QRCode(box_size=4, border=2)
    qr.add_data(url)
    qr.make(fit=True)
    img = qr.make_image(fill_color="black", back_color="white")
    buf = io.BytesIO()
    img.save(buf, format="PNG")
    buf.seek(0)
    return Image(buf, width=1.2 * inch, height=1.2 * inch)


